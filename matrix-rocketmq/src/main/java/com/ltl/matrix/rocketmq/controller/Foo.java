package com.ltl.matrix.rocketmq.controller;

public class Foo {
	private Integer index;
	private String name;
	
	public Foo(Integer index, String name) {
		super();
		this.index = index;
		this.name = name;
	}
	public Integer getIndex() {
		return index;
	}
	public void setIndex(Integer index) {
		this.index = index;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
